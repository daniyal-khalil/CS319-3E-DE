package sample;

public class Object
{
    // variables
    private int yPos;
    private int xPos;
    private int size;

    // methods
    public void setXPos(int xPos)
    {
        this.xPos = xPos;
    }

    public void setYPos(int yPos)
    {
        this.yPos = yPos;
    }

    public int getYPos()
    {
        return yPos;
    }

    public int getXPos()
    {
        return xPos;
    }

    public int getSize()
    {
        return size;
    }

    public void setSize(int size)
    {
        this.size = size;
    }
}
